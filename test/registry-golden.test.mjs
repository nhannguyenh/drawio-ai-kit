// Pins the per-type layout registry refactor (issue #44) as structural, NOT numerical.
// This pins the registry refactor as structural-not-numerical: if coordinates drift, the
// refactor changed math, not structure. The snapshot below is the exact x/y/w/h of every node
// in a representative multi-kind tree (group + grid + pool + icon + box, nested) computed by
// the OLD switch-based engine, frozen so any drift in the registry refactor fails here.
import { test } from "node:test";
import assert from "node:assert/strict";
import { icon, box, group, grid, pool, renderTree } from "../src/layout-engine.mjs";
import { Diagram } from "../src/builder.mjs";

function buildTree() {
  return group("root", null, "Region", { dir: "col", gap: 30 }, [
    group("svc", "group_region", "Services", { dir: "row", gap: 20 }, [
      icon("ic1", "s3", "Bucket"),
      box("b1", "Source\nendpoint"),
      grid("g1", null, "Cells", { cols: 2, gap: 16 }, [
        box("gc1", "A"), box("gc2", "BB"), box("gc3", "CCC"), box("gc4", "DDDD"),
      ]),
    ]),
    pool("p1", "Flow", { lanes: ["Lane0", "Lane1"], phases: ["Start", "End"] }, [
      { kind: "box", id: "n00", label: "T0", lane: 0, col: 0, w: 120, h: 44 },
      { kind: "box", id: "n01", label: "T1", lane: 0, col: 1, w: 120, h: 44 },
      { kind: "box", id: "n10", label: "T2", lane: 1, col: 0, w: 120, h: 44 },
      { kind: "box", id: "n11", label: "T3", lane: 1, col: 1, w: 120, h: 44 },
    ]),
  ]);
}

